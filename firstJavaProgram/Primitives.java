package com.pratik;

public class Primitives {
    public static void main(String[] args) {

        // data type which we cannot break into further data type is called Primitive data type.
        // eg : int,char

        int rollNumber = 20;
        char letter = 'p';
        float marks = 54.87f;
        double largeDecimalNumber = 23928.2382392;
        long largeIntegerNumber = 72623892322334L;
        boolean check = true;
    }
}
